girdi=input("Enter your input and it shall be binary (10 digits accuracy!!!): ")

girdi=str(girdi)

tam=girdi.split(".")[0]
kesir=girdi.split(".")[1]

bolum=int(tam)

tam_cevap=""
while True:
    kalan=bolum%2
    tam_cevap=str(kalan)+tam_cevap
    bolum=bolum//2
    if(bolum==0):
        break


kesir_cevap=""
kesirr=float(kesir)/(10**len(kesir))

for i in range(10):
    kesirr=2*kesirr
    
    if(kesirr<1):
        kesir_cevap=kesir_cevap+"0"
    else:
        kesir_cevap=kesir_cevap+"1"
        kesirr=kesirr-1
    

cevap=tam_cevap+"."+kesir_cevap

print("Behold! Here is your binary number: %s"%cevap)
