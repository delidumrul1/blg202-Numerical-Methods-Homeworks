import numpy as np

def func(x,t):
    return (np.power(x,5) - t )

A=float(input("Give me number 'a' and I shall find the 'a^(1/5)' for you my lady: "))
epsilon=float(input("What should our tolerance(epsilon) be ??? : "))
print("Warning! 100000 is the max iteration number.")
a=0
b=0
c=0
#[a,b] a<=b                                                             

if a>=0:
    a=0
    b=A
else:
    a=A
    b=0


max_i=100000
i=1

while i<=max_i and (b-a)>epsilon:
    c=(a+b)/2
    if func(c,A)==0:
        break
    #We found the c  !!!

    i+=1
    if func(c,A)>0:
        b=c
    else:
        a=c

print("Number of iterations: %s"%i)
print("Our last interval size: %s"%(b-a))
comp_ans=np.power(A,(1/5))
print("What numpy did give us : %s"%comp_ans)
print("OUR ANSWER : %s"%c)
dif=c-comp_ans
print("Difference between our answer and numpy's: %s"%dif)
input("Hit enter to finish the program.")
